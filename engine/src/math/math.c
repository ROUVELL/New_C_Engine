#include "math.h"

#include <math.h>

f32 msin(f32 x) {
    return sinf(x);
}

f32 mcos(f32 x) {
    return cosf(x);
}

f32 mtan(f32 x) {
    return tanf(x);
}

f32 masin(f32 x) {
    return asinf(x);
}

f32 macos(f32 x) {
    return acosf(x);
}

f32 matan(f32 x) {
    return atanf(x);
}

f32 msqrt(f32 x) {
    return sqrtf(x);
}

f32 mabs(f32 x) {
    return fabsf(x);
}
